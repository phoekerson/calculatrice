export const PI = Math.PI;

export function calculateSquareRoot(x: number): number {
  return Math.sqrt(x);
}

export function calculatePower(base: number, exp: number): number {
  return Math.pow(base, exp);
}

export function calculateSin(x: number): number {
  return Math.sin(x);
}

export function calculateCos(x: number): number {
  return Math.cos(x);
}

export function calculateTan(x: number): number {
  return Math.tan(x);
}

export function calculateLog(x: number): number {
  return Math.log10(x);
}
